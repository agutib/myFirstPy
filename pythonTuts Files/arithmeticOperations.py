"""
+       - Addition
-       - Subtraction
*       - Multiplication

There are three kinds of division in python
/       - Division
//      - Division that returns only an Integer
%       - Division that returns only a remainder
"""
#Addition
print(10 + 10)

#Subtraction
print(20 - 10)

#Multiplication
print(5 * 5)

#Division
print(20 / 6)

#Division returns whole number only ( Integer )
print(20 // 6)

#Division returns only a remainder
print(20 % 6)